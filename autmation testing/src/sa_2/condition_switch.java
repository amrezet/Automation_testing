package sa_2;

public class condition_switch {
    public static void main(String[] args) {
        int number =15;
        switch (number)
        {
            case 5 :
                System.out.println("5");
                break;
            case 10 :
                System.out.println("10");
                break;
            case 15 :
                System.out.println("15");
                break;
            default:
                System.out.println("condition_switch.main");

        }
    }
}
